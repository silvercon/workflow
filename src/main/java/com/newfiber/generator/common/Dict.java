public class Dict {

    private String key;

    private String value;

    public String getKey() {
        return key;
    }

    public Dict setKey(String key) {
        this.key = key;
        return this;
    }

    public String getValue() {
        return value;
    }

    public Dict setValue(String value) {
        this.value = value;
        return this;
    }
}
